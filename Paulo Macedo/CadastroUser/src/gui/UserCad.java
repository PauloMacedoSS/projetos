    public void popularTabela(String sql){
        try{
            Connection conn = (Connection)DriverManager.getConnection("jdbc:mysql://localhost/caduser", "root", "Rasen*12");
            PreparedStatement banco = (PreparedStatement)conn.prepareStatement(sql);
            banco.execute();//Cria o vetor.
            ResultSet resul = banco.executeQuery(sql);
            
            DefaultTableModel model = (DefaultTableModel) tblConsulta.getModel();
            model.setNumRows(0);
            
            while(resul.next()){
                model.addRow(new Object[]{
                    //retorna os dados da tabela do DB, cada campo em uma coluna.
                    resul.getString("id"),
                    resul.getString("nome"),
                    resul.getString("trabalho"),
                    resul.getString("idade")
                });
            }
        banco.close();
        conn.close();
        }
        catch(SQLException ex){
            System.out.println("Houve um erro: " + ex);
        }
    }
    
