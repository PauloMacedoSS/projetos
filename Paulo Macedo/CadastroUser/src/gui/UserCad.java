    private void btnCadActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadActionPerformed
        // TODO add your handling code here:
        //Instanciando a classe Usuario do pacote model e criando seu objeto usuarios.
        Usuario users = new Usuario();
        users.setNome(txtNome.getText());
        users.setTrabalho(txtTrabalho.getText());
        users.setIdade((int) spIdade.getValue());
        
        //Fazendo a valida√ß√£o dos dados.
        if((txtNome.getText().isEmpty()) || txtTrabalho.getText().isEmpty() || spIdade.toString().isEmpty()){
            JOptionPane.showMessageDialog(null, "O cadastro n√£o pode ser realizado se algum campo estiver vazio!");
        }else{
            //Instanciando a classe UsuarioDAO do pacote dao e criando seu objeto dao.
            UsuarioDAO dao = new UsuarioDAO();
            dao.add(users);
            JOptionPane.showMessageDialog(null, "Usu√°rio " + txtNome.getText() + " inserido com sucesso!");
        }
        //Apaga dados preenchidos no campo de texto.
        txtNome.setText("");
        txtTrabalho.setText("");
        spIdade.setValue(0);
    }//GEN-LAST:event_btnCadActionPerformed

